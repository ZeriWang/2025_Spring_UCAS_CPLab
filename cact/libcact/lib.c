#include <stdio.h>
void get_int() {
    int x;
    scanf("%d", &x);
}

void get_float() {
    float x;
    scanf("%f", &x);
}

void get_char() {
    char x;
    scanf("%c", &x);
}

void print_int(int x) {
    printf("%d", x);
}

void print_float(float x) {
    printf("%f", x);
}

void print_char(char x) {
    printf("%c", x);
}